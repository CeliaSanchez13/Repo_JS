// Operadores de Asignacion
let miNumero = 10;
console.log(miNumero); //10

miNumero = 20;
console.log(miNumero); //20

// Operador +=
miNumero += 5;
console.log(miNumero); //25

// Operador -=
miNumero -= 3;
console.log(miNumero); //23

// Operador *=
miNumero *= 2;
console.log(miNumero); //44

// Operador /=
miNumero /= 4;
console.log(miNumero); //11

// Operador %=
miNumero %= 2;
console.log(miNumero); // 1 

// Exponente **=
miNumero = 8;
miNumero **= 2;// 8 * 8 = 64
console.log(miNumero);
